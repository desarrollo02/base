package py.com.ping.utils.dto;

/**
 *
 * @author rudy
 */
public class ProveedorDto {

    private String direccionABM;
    private String codigoPostalABM;
    private String nombreContactoABM;
    private String emailABM;
    private String telefonoABM;
    private String paisABM;
    private String ciudadABM;

    public ProveedorDto() {
    }

    public String getDireccionABM() {
        return direccionABM;
    }

    public void setDireccionABM(String direccionABM) {
        this.direccionABM = direccionABM;
    }

    public String getCodigoPostalABM() {
        return codigoPostalABM;
    }

    public void setCodigoPostalABM(String codigoPostalABM) {
        this.codigoPostalABM = codigoPostalABM;
    }

    public String getNombreContactoABM() {
        return nombreContactoABM;
    }

    public void setNombreContactoABM(String nombreContactoABM) {
        this.nombreContactoABM = nombreContactoABM;
    }

    public String getEmailABM() {
        return emailABM;
    }

    public void setEmailABM(String emailABM) {
        this.emailABM = emailABM;
    }

    public String getTelefonoABM() {
        return telefonoABM;
    }

    public void setTelefonoABM(String telefonoABM) {
        this.telefonoABM = telefonoABM;
    }

    public String getPaisABM() {
        return paisABM;
    }

    public void setPaisABM(String paisABM) {
        this.paisABM = paisABM;
    }

    public String getCiudadABM() {
        return ciudadABM;
    }

    public void setCiudadABM(String ciudadABM) {
        this.ciudadABM = ciudadABM;
    }

    
}
